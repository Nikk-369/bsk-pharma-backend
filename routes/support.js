const express = require("express");
const { createSupport , readAllSupport , updateSupport , readSupport , deleteSupport  } = require("../controllers/supportControllers");

const router = express.Router();

router.post("/createSupport", createSupport);
router.post("/readallSupport", readAllSupport);
router.put("/updateSupport/:id", updateSupport);
router.get("/readSupport/:id", readSupport);
router.delete("/deleteSupport/:id", deleteSupport);

module.exports = router;